export default {
  data: () => ({
    email: "",
    password: "",
    alertOpen: false,
    errorMessage: ""
  }),
  mounted() {
    if (this.$supabase.auth.user()) {
      this.$router.push("/");
    }
  },
  methods: {
    async handleLogin() {
      try {
        const { error } = await this.$supabase.auth.signUp({
          email: this.email,
          password: this.password
        });
        if (error) throw error;
        else this.$router.push("/");
      } catch (error) {
        this.alertOpen = true;
        this.errorMessage = error.error_description || error.message;
      } finally {
      }
    },
    closeAlert() {
      this.alertOpen = false;
    }
  }
};
</script>
